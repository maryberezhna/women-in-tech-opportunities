export function getDict(locale) {
  return dict[locale] || dict[DEFAULT_LOCALE];
}

// Read a localized field from an opportunity row.
//   loc(item, 'title', 'uk')  →  item.title_uk || item.title || ''
export function loc(item, field, locale) {
  if (!item) return '';
  return item[`${field}_${locale}`] || item[field] || '';
}
